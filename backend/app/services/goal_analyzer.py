import re
from typing import Dict, Any

def analyze_custom_text(text: str) -> Dict[str, Any]:
    """
    Анализирует свободный текст цели и возвращает:
    - предполагаемую категорию (на основе ключевых слов)
    - ключевые слова
    - уверенность (количество совпадений)
    """
    text_lower = text.lower()
    
    # Категории с ключевыми словами (более точные списки)
    categories = {
        "образование": [
            "учит", "изуча", "курс", "книг", "читать", "лекци", "школ", 
            "универ", "диплом", "python", "программирова", "код", "разработк"
        ],
        "карьера": [
            "работ", "карьер", "повышен", "проект", "клиент", "бизнес", 
            "заработ", "должност", "ваканс", "трудоустройств"
        ],
        "здоровье": ["здоров", "спорт", "фитнес", "тренировк", "бег", "йога", "питани", "вес", "мышц"],
        "творчество": ["творч", "рисоват", "писат", "музык", "инструмент", "танц"],
        "финансы": ["деньг", "финанс", "копит", "инвестиц", "бюджет", "трат"],
    }

    # Подсчитываем совпадения для каждой категории
    scores = {}
    for cat, keywords in categories.items():
        score = 0
        for kw in keywords:
            if kw in text_lower:
                score += 1
        if score > 0:
            scores[cat] = score

    # Если нет совпадений, возвращаем "другое"
    if not scores:
        return {
            "category": "другое",
            "keywords": {},
            "confidence": 0,
        }

    # Выбираем категорию с максимальным числом совпадений
    best_cat = max(scores, key=scores.get)
    
    # Особый случай: если есть и образование, и карьера, но ключевых слов образования больше,
    # то оставляем образование (как в твоём примере с Python)
    if "образование" in scores and "карьера" in scores:
        if scores["образование"] >= scores["карьера"]:
            best_cat = "образование"
        else:
            best_cat = "карьера"

    return {
        "category": best_cat,
        "keywords": scores,
        "confidence": scores[best_cat],
    }